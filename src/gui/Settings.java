    private void jTextFieldOutputPoDirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextFieldOutputPoDirMouseClicked
        final JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        
        int returnVal = fc.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            jTextFieldOutputPoDir.setText(fc.getSelectedFile().getAbsolutePath()+"");
        }
    }//GEN-LAST:event_jTextFieldOutputPoDirMouseClicked

    private void jButtonContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonContinueActionPerformed
        
        if(jTextFieldOutputPoDir.getText().isEmpty()){
           JOptionPane.showMessageDialog(this, "Please insert output page objects directory");
           return;
        }
        int runtime = Integer.parseInt(jSpinnerCrawlingRuntime.getValue().toString());
        if(runtime < 1){
            JOptionPane.showMessageDialog(this, "Crawling runtime incorrect");
            return;
        }
        int browsers = Integer.parseInt(jSpinnerNumBrowsers.getValue().toString());
        if(browsers < 1){
            JOptionPane.showMessageDialog(this, "Number of browsers incorrect");
            return;
        }
        
        // SAVE (SET) CONFIGS
        configFile.setProperty("GEN_PO_DIR", jTextFieldOutputPoDir.getText());
        configFile.setProperty("NUMBER_OF_BROWSERS", Integer.toString((int) jSpinnerNumBrowsers.getValue()));
        configFile.setProperty("MAX_RUNTIME", Integer.toString((int) jSpinnerCrawlingRuntime.getValue()));
        
        configFile.setProperty("OUT_DIR", "output/");
        configFile.setProperty("DOMS_DIR", configFile.getProperty("OUT_DIR") + "doms/");
        configFile.setProperty("CLUST_DIR", configFile.getProperty("OUT_DIR") + "clusters/");
        configFile.setProperty("WAIT_TIME_AFTER_EVENT", "500");
        configFile.setProperty("WAIT_TIME_AFTER_RELOAD", "500");
        configFile.setProperty("CRAWL_DEPTH", "0");
        configFile.setProperty("MAX_STATES", "0");
        
        try {
            APOGEN.saveAppConfigFile(configFile, "src/config.properties");
        } catch (IOException ex) {
            Logger.getLogger(Settings.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        // CLOSE GUI AND LAUNCH APOGEN
        this.setVisible(false);
        
        apogen.ApogenMain apogen = new apogen.ApogenMain();
        try {
            apogen.run();
        } catch (Exception ex) {
            Logger.getLogger(Settings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonContinueActionPerformed

