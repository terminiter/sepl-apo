    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        
        final JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
                       
            String ext = FilenameUtils.getExtension(file.getAbsolutePath());
            
            if(ext.equals("properties")){
                System.out.println("Reading: " + file.getName() + "\n");
               
                Properties configFile = new Properties();
            
                try {
                    configFile.load(Crawler.class.getClassLoader().getResourceAsStream(file.getName()));
                    
                    jTextFieldUrl.setText(configFile.getProperty("URL"));
                    jTextFieldUserField.setText(configFile.getProperty("USERNAME_FIELD"));
                    jTextFieldUserValue.setText(configFile.getProperty("USERNAME_VALUE"));
                    jTextFieldPassField.setText(configFile.getProperty("PASSWORD_FIELD"));
                    jTextFieldPassValue.setText(configFile.getProperty("PASSWORD_VALUE"));
                    jTextFieldSubmitField.setText(configFile.getProperty("SUBMIT_FIELD"));
                    jTextFieldSubmitValue.setText(configFile.getProperty("SUBMIT_TEXT"));
                    
                    setAppConfigFile(configFile);
                    
                } catch (IOException e) {
                    e.printStackTrace();
                }
            
            } 
            else {
                JOptionPane.showMessageDialog(this,
                    "Invalid file extension.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            }
        } else {
            
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jButtonContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonContinueActionPerformed
        
        appConfigFile.setProperty("URL", jTextFieldUrl.getText());
        appConfigFile.setProperty("USERNAME_FIELD", jTextFieldUserField.getText());
        appConfigFile.setProperty("USERNAME_VALUE", jTextFieldUserValue.getText());
        appConfigFile.setProperty("PASSWORD_FIELD", jTextFieldPassField.getText());
        appConfigFile.setProperty("PASSWORD_VALUE", jTextFieldPassValue.getText());
        appConfigFile.setProperty("SUBMIT_FIELD", jTextFieldSubmitField.getText());
        appConfigFile.setProperty("SUBMIT_TEXT", jTextFieldSubmitValue.getText());
        
        try {
            saveAppConfigFile(appConfigFile, "src/app.properties");
            System.out.println(appConfigFile);
            Crawler.setAppConfigFile(appConfigFile);
        } catch (IOException ex) {
            Logger.getLogger(APOGEN.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        // LAUNCH OTHER GUI
        Settings njf = new Settings();
        njf.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButtonContinueActionPerformed

