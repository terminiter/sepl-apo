    /**
     * save the configurations into the file specified
     * @param appConfigFile
     * @param name
     * @throws FileNotFoundException
     * @throws IOException
     */
    public static void saveAppConfigFile(Properties appConfigFile, String name) throws FileNotFoundException, IOException{
        
        System.out.println("[LOG] Saving: " + name);
        File f = new File(name);
        
        try (OutputStream out = new FileOutputStream(f)) {
            appConfigFile.store(out, "");
            System.out.println("[LOG] Saved: " + name);
        }
   
    }

